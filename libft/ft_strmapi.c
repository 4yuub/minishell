#include <stdlib.h>
#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char				*t;
	unsigned int		i;

	if (!s)
		return (0);
	t = malloc(ft_strlen(s) + 1);
	if (!t)
		return (t);
	i = 0;
	while (s[i])
	{
		t[i] = f(i, s[i]);
		i++;
	}
	t[i] = '\0';
	return (t);
}
